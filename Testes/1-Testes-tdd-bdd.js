//TDD (Test Driven Development):
/*
-->Cria um teste antes de escrever o código. Consiste em refatorar em pequenos ciclos.
-->Escrita do teste -> Escrita do código -> Refatoração

VANTAGENS:
-->FeedBack rapido;
-->Maior segurança em alterações e novas funcionalidades;
-->Codigo mais limpos;
-->Produtividade.
*/

